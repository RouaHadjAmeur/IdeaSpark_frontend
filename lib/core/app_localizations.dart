    'audience': {'Étudiants': 'Students', 'Mamans': 'Moms', 'Entrepreneurs': 'Entrepreneurs', 'Freelancers': 'Freelancers'},
    'platform': {'TikTok': 'TikTok', 'Reels': 'Reels', 'YouTube': 'YouTube', 'Shorts': 'Shorts'},
