    'filter': {'Tous': 'All', 'Viral': 'Viral', 'Réaliste': 'Realistic', 'Courtes': 'Short'},
  };

  static String translate(String key, String locale) {
    final map = locale == 'en' ? _en : _fr;
    return map[key] ?? _fr[key] ?? key;
  }

  static String translateOption(String locale, String category, String value) {
    if (locale != 'en') return value;
    final cat = _optionEn[category];
    return cat?[value] ?? value;
  }
}

extension AppLocalizationsExtension on BuildContext {
  String tr(String key) {
    final localeVm = read<LocaleViewModel>();
    return AppLocalizations.translate(key, localeVm.locale);
  }

  String trOption(String category, String value) {
    final localeVm = read<LocaleViewModel>();
    return AppLocalizations.translateOption(localeVm.locale, category, value);
  }
}
